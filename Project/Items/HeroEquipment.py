class HeroEquipment:
    def __init__(self, helmet, chest, pants, boots, weapon_1, weapon_2, jewel_1, jewel_2,
                 total_protection_points, total_dodge_rate, total_parry_rate, total_critical_hit,
                 total_min_damage, total_max_damage):
        self.helmet = helmet
        self.chest = chest
        self.pants = pants
        self.boots = boots
        self.weapon_1 = weapon_1
        self.weapon_2 = weapon_2
        self.jewel_1 = jewel_1
        self.jewel_2 = jewel_2
        self.total_protection_points = total_protection_points
        self.total_dodge_rate = total_dodge_rate
        self.total_parry_rate = total_parry_rate
        self.total_critical_hit = total_critical_hit
        self.total_min_damage = total_min_damage
        self.total_max_damage = total_max_damage
